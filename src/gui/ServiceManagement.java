    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {

            String serviceName = jTextField1.getText();
            String serviceType = String.valueOf(jComboBox1.getSelectedItem());
            String duration = jTextField2.getText();
            String price = jTextField3.getText();
            String description = jTextArea1.getText();
            
            if (serviceName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter a Service Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (serviceType.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select a Service Type", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (duration.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter the Duration", "Warning", JOptionPane.WARNING_MESSAGE);
            }else if (price.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please the Price", "Warning", JOptionPane.WARNING_MESSAGE);
            }else if (description.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter a Description", "Warning", JOptionPane.WARNING_MESSAGE);
            }else {

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `service` "
                        + "INNER JOIN `service_type` ON `service`.`service_type_id` = `service_type`.`id`  "
                        + "WHERE `service`.`name` = '" + serviceName + "' ");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This Service is already registered", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    MySQL2.executeIUD("INSERT INTO `service` (`name`,`service_type_id`,`duration`,`description`,`price`) "
                            + "VALUES('" + serviceName + "','" + serviceTypeMap.get(serviceType) + "','" + duration + "','" + description + "','" + price + "')");

                    loadServices("price", "ASC", jTextField2.getText());
                    reset();               

                }

            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

