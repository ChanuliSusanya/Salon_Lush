    private void loadServiceType(){
    
        try {
            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `service_type`");
            
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            
            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                serviceTypeMap.put(resultSet.getString("name"), resultSet.getString("id"));
                
                DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
                jComboBox1.setModel(model);
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
    
    private void loadServices(String column, String orderby, String duration){
    
        try {
            
            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `service` "
                    + "INNER JOIN `service_type` ON `service`.`service_type_id` = `service_type`.`id` "
                    + "WHERE `service`.`duration` LIKE '" + duration + "%' "
                            + "ORDER BY `" + column + "` " + orderby );
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("service.id"));
                vector.add(resultSet.getString("service.name"));
                vector.add(resultSet.getString("service_type.name"));
                vector.add(resultSet.getString("duration"));
                vector.add(resultSet.getString("price"));
                vector.add(resultSet.getString("description"));
                
                model.addRow(vector);
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }

