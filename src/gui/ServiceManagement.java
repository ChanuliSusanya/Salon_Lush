    private void reset() {
        jTextField1.setText("");
        jComboBox1.setSelectedIndex(0);
        jTextField2.setText("");
        jTextField3.setText("");
        jTextArea1.setText("");
        jTextField1.setEditable(true);
        jComboBox1.setEditable(true);
        search();
        
    }

    private void search() {
        
        int sort = jComboBox1.getSelectedIndex();

        if (sort == 0) {
            loadServices("price", "ASC", jTextField2.getText());
        } else if (sort == 1) {
            loadServices("price", "DESC", jTextField2.getText());
        } else if (sort == 2) {
            loadServices("duration", "ASC", jTextField2.getText());
        } else if (sort == 3) {
            loadServices("duration", "DESC", jTextField2.getText());
        }
        
    }
