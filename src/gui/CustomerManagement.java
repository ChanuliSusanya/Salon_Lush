    private void reset() {
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField1.grabFocus();
        jTextField3.setEditable(true);
        jTable1.clearSelection();
        search();
        
    }
    
    private void search() {
        int sort = jComboBox1.getSelectedIndex();
        int row = jTable1.getSelectedRow();
        
        if (sort == 0) {
            loadCustomers("first_name", "ASC", jTextField1.getText());
        } else if (sort == 1) {
            loadCustomers("first_name", "DESC", jTextField1.getText());
        } else if (sort == 2) {
//            change into loyalty points
            loadCustomers("loyalty_points", "ASC", jTextField1.getText());
        } else if (sort == 3) {
//            change into loyalty points
            loadCustomers("loyalty_points", "DESC", jTextField1.getText());
            
        }
    }
    
