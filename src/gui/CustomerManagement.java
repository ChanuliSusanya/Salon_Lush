    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String firstName = jTextField1.getText();
        String lastName = jTextField2.getText();
        String mobile = jTextField3.getText();
        String email = jTextField4.getText();
        
        if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^(?:0|94|\\+94|0094)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|91)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.matches("^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,}$")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Email", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else {
            try {
                
                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '" + mobile + "' OR `email` = '" + email + "'");
                
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Customer already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                    
                } else {
                    
                    MySQL2.executeIUD("INSERT INTO `customer`(`first_name`,`last_name`,`mobile`,`email`)"
                            + "VALUES('" + firstName + "','" + lastName + "','" + mobile + "','" + email + "')");
                    
                    loadCustomers("first_name", "ASC", jTextField1.getText());
                    reset();
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String firstName = jTextField1.getText();
        String lastName = jTextField2.getText();
        String mobile = jTextField3.getText();
        String email = jTextField4.getText();
        
        if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.matches("^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,}$")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Email", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else {
            
            try {
                
                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `email` = '" + email + "'");
                
                boolean canUpdate = false;
                
                if (resultSet.next()) {
                    if (resultSet.getString("mobile").equals(mobile)) {
                        canUpdate = true;
                    } else {
                        
                        JOptionPane.showMessageDialog(this, "Customer already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                        
                    }
                    
                } else {
                    
                    canUpdate = true;
                }
                
                if (canUpdate) {
                    MySQL2.executeIUD("UPDATE `customer` SET "
                            + "`first_name` = '" + firstName + "',`last_name` = '" + lastName + "',`email` = '" + email + "' "
                            + "WHERE `mobile` = '" + mobile + "'");
                    
                    loadCustomers("first_name", "ASC", jTextField1.getText());
                    reset();
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int row = jTable1.getSelectedRow();
        
        String fname = String.valueOf(jTable1.getValueAt(row, 1));
        String lname = String.valueOf(jTable1.getValueAt(row, 2));
        String mobile = String.valueOf(jTable1.getValueAt(row, 3));
        String email = String.valueOf(jTable1.getValueAt(row, 4));
        
        jTextField1.setText(fname);
        jTextField2.setText(lname);
        jTextField3.setText(mobile);
        jTextField3.setEditable(false);
        jTextField4.setText(email);
        
        try {
            ResultSet resultSet = MySQL2.executeSearch("SELECT COUNT(transaction_log.id) FROM `appoinment` "
                    + "INNER JOIN `customer` ON `appoinment`.`customer_id` = `customer`.`id` "
                    + "INNER JOIN `transaction` ON `transaction`.`appoinment_id` = `appoinment`.`id` "
                    + "INNER JOIN `transaction_log` ON `transaction_log`.`transaction_id` = `transaction`.`id`"
                    + "WHERE `email` = '" + email + "'");
            
            if (resultSet.next()) {
                jLabel9.setText(resultSet.getString(1));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        

    }//GEN-LAST:event_jTable1MouseClicked

