    private void reset() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField1.grabFocus();
        search();
    }

    private void search() {
        int sort = jComboBox1.getSelectedIndex();

        if (sort == 0) {
            loadBranches("appoinment_count", "ASC", jTextField2.getText());
        } else if (sort == 1) {
            loadBranches("appoinment_count", "DESC", jTextField2.getText());
        } else if (sort == 2) {
            loadBranches("staff_count", "ASC", jTextField2.getText());
        } else if (sort == 3) {
            loadBranches("staff_count", "DESC", jTextField2.getText());
        }
    }
