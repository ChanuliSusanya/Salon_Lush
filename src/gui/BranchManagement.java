    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String location = jTextField1.getText();
        String hotline = jTextField2.getText();

        if (hotline.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Hotline Number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!hotline.matches("^(?:0|94|\\+94|0094)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|91)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "Please enter valid Hotline Number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (location.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Location", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `branch` WHERE `name` = '" + location + "' OR `hotline` = '" + hotline + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Branch already registerd", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL2.executeIUD("INSERT INTO `branch` (`name`,`hotline`) VALUES ('" + location + "','" + hotline + "') ");

                    loadBranches("Appoinment_count", "ASC", jTextField1.getText());
                    reset();
                    
                    

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

