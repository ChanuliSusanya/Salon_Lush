    private void loadBranches(String column, String orderby, String hotline) {

        try {
            ResultSet resultSet = MySQL2.executeSearch("SELECT `branch`.`id`, `branch`.`name`, `branch`.`hotline`, " +
            "COUNT(`staff`.`nic`) AS staff_count, " +
            "COUNT(`appoinment`.`id`) AS appoinment_count " +
            "FROM branch " +
            "LEFT JOIN `staff` ON `staff`.`branch_id` = `branch`.`id` " +
            "LEFT JOIN appoinment ON `appoinment`.`branch_id` = `branch`.`id` "
                    + "WHERE `branch`.`hotline` LIKE '" + hotline + "%' "
                            + "GROUP BY `branch`.`id`, `branch`.`name`, `branch`.`hotline` "
                            + "ORDER BY `" + column + "` " + orderby);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("branch.id"));
                vector.add(resultSet.getString("branch.name"));
                vector.add(resultSet.getString("branch.hotline"));
                vector.add(resultSet.getString("staff_count"));
                vector.add(resultSet.getString("appoinment_count"));

                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

